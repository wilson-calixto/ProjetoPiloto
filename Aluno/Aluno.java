package monitoria;

public class Aluno {
	
	String nome;
	int matricula;
	String atividade;
	
	void insereNome(String NomeDoAluno){
		nome=NomeDoAluno;	
	}
	
	void insereMatricula(int NumeroDaMatricula){
		matricula=NumeroDaMatricula;
	}
	
	void insereAtividade(String AtividadeDoAluno){
		atividade=AtividadeDoAluno;
		
	}
}


